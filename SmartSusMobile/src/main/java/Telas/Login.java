    public Paciente logar(){
        Paciente p = new Paciente();
        
        if(!UsuarioDAO.FazerLogin(login.getText(), new String(senha.getPassword()))){
            return null;
        }
        int id=UsuarioDAO.lerIdUser(login.getText());
        p = PacienteDAO.retornarCadastro(id);
        return p;
    }

