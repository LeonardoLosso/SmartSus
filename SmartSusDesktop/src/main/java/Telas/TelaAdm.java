    public void readFullFuncionario(){
        DefaultTableModel listaF = (DefaultTableModel) tbFunc.getModel();
        DefaultTableModel listaFUser = (DefaultTableModel) tbFuncUser.getModel();
        listaF.setNumRows(0);
        listaFUser.setNumRows(0);
        
        FuncionarioDAO fdao = new FuncionarioDAO();
        
        for(Funcionario f : fdao.lerListaFuncionario()){
            listaF.addRow(new Object[]{
                f.getMatricula(),
                f.getTipo(),
                f.getNome(),
                f.getExp(),
                f.getTelefone()
            });
            listaFUser.addRow(new Object[]{
                f.getId(),
                f.getMatricula(),
                f.getNome()
            });
        }
    }
  

