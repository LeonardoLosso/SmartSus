    private void btnCadastraUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastraUserActionPerformed
        UsuarioDAO udao = new UsuarioDAO();
        Usuario u = new Usuario();
        String s = new String(senha.getPassword()); //casta o passfield char pra string
        String sc = new String(confirmaSenha.getPassword());
        Funcionario f = new Funcionario();

        if(udao.confereLogin(nomeUsuario.getText())){
            JOptionPane.showMessageDialog(null, "Nome de Usuario: " +nomeUsuario.getText()+ " já esta sendo utilizado!");
        }else{
            if(!(s.equals(sc))){
                JOptionPane.showMessageDialog(null, "Os campos SENHA e CONFIRMA SENHA são diferentes!");
            }else{
                f.setId((int) tbFuncUser.getValueAt(tbFuncUser.getSelectedRow(), 0));
                u.setEmail(email.getText());
                u.setLogin(nomeUsuario.getText());
                u.setSenha(s);
                u.setPessoa(f);

                udao.salvarUsuario(u);
                limpaCampoUsuarios();
            }
        }
    }//GEN-LAST:event_btnCadastraUserActionPerformed

