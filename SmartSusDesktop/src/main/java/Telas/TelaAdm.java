    private void cadastrarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarFuncActionPerformed
        Endereco e = new Endereco();
        Funcionario f = new Funcionario();
        e.setBairro(bairro.getText());
        e.setCep(cep.getText());
        e.setEndereco(endereco.getText());
        e.setComplemento(complemento.getText());
        e.setNumero(Integer.parseInt(numLogra.getText()));
        e.setUF(uf.getText());

        f.setEndereco(e);
        f.setCpf(cpf.getText());
        f.setTipo(tipoCadastro.getSelectedItem().toString().substring(0, 1));//pega o primeiro caractere
        f.setTelefone(telefone.getText());
        f.setNome(nomeFunc.getText());

        int doc = "".equals(documento.getText())? 0 : Integer.parseInt(documento.getText());
        f.setDocumento(doc);
        f.setMatricula(Integer.parseInt(numMatricula.getText()));
        f.setExp(especializacao.getText());

        FuncionarioDAO salvar = new FuncionarioDAO();
        EnderecoDAO salvar2 = new EnderecoDAO();
        salvar2.salvarCep(e);
        salvar.salvarCadastro(f);

        limparCamposTelaCadastro();
        numMatricula.setText(Integer.toString(f.getMatricula()));
        readFullFuncionario();
    }//GEN-LAST:event_cadastrarFuncActionPerformed

