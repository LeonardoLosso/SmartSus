    public void setListaUnidades(){
        UnidadeDAO udao = new UnidadeDAO();
        
        for(UnidadeUbs u : udao.lerListaUnidadeUbs()){
            unidades.addItem(u.getId()+"  "+u.getNome());
        }
    }
    
    public Funcionario logar(){
        UsuarioDAO udao = new UsuarioDAO();
        Funcionario f = new Funcionario();
        FuncionarioDAO fdao= new FuncionarioDAO();
        
        if(!udao.FazerLogin(login.getText(), new String(senha.getPassword()))){
            return null;
        }
        int id=udao.lerIdUser(login.getText());
        f = fdao.retornarCadastro(id);
        return f;
    }

