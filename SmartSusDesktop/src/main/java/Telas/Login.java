    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        Logado.setFuncionario(logar());
        if(Logado.getFuncionario()==null){
            JOptionPane.showMessageDialog(null, "Credenciais Incorretas!");
        }else{
            if(unidades.getSelectedIndex() == 0){
                JOptionPane.showMessageDialog(null, "Unidade selecionada inv√°lida");
            }else{
                JOptionPane.showMessageDialog(null, "Login realizado com sucesso!");
                
                String tipo = Logado.getFuncionario().getTipo();
                
                UnidadeDAO u = new UnidadeDAO();
                Logado.setUpa(u.getUnidade(unidades.getSelectedIndex()));

                if(tipo.equals("M") || tipo.equals("D")){
                    TelaDoutores telaDoc = new TelaDoutores();
                    telaDoc.setVisible(true);
                    
                }else{
                    TelaAdm telaAdm = new TelaAdm();
                    telaAdm.setVisible(true);           
                }
                this.dispose();
            }
        }
    }//GEN-LAST:event_entrarActionPerformed

